let _lang = localStorage.getItem('meetscribe_lang') || 'en';

function t(key) {
  return LANG[_lang]?.[key] ?? LANG.en[key] ?? key;
}

function setLang(lang) {
  if (!LANG[lang]) return;
  _lang = lang;
  localStorage.setItem('meetscribe_lang', lang);
}

function getCurrentLang() {
  return _lang;
}
